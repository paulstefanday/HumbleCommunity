angular.module('MyApp')
  .controller('AddCtrl', function($scope, $auth, $alert, Account, $http, $sce) {

$scope.jobs = {};

$scope.search = function() {
  $http.jsonp('http://api.adzuna.com:80/v1/api/jobs/au/search/1?app_id=4d5ed715&app_key=9e0bb8521ad6e3c5e82276ffba08d48a&callback=JSON_CALLBACK&results_per_page=100&what='+ encodeURIComponent( $scope.keyword ) +'&content-type=application/jsonp').then(function(resp) {
        $scope.jobs = resp.data.results;
  });
}


    $scope.viewJob = function (job) {
      $scope.currentJob = $sce.trustAsResourceUrl(job.redirect_url);
      //set each var seperately as names differ
      $scope.job = job;
      console.log(job);
    }


    /**
     * Get user's profile information.
     */
    $scope.getProfile = function() {
      Account.getProfile()
        .success(function(data) {
          $scope.user = data;
        })
        .error(function(error) {
          $alert({
            content: error.message,
            animation: 'fadeZoomFadeDown',
            type: 'material',
            duration: 3
          });
        });
    };


    /**
     * Update user's profile information.
     */
    $scope.updateProfile = function() {
      Account.updateProfile({
        displayName: $scope.user.displayName,
        email: $scope.user.email
      }).then(function() {
        $alert({
          content: 'Profile has been updated',
          animation: 'fadeZoomFadeDown',
          type: 'material',
          duration: 3
        });
      });
    };

    /**
     * Link third-party provider.
     */
    $scope.link = function(provider) {
      $auth.link(provider)
        .then(function() {
          $alert({
            content: 'You have successfully linked ' + provider + ' account',
            animation: 'fadeZoomFadeDown',
            type: 'material',
            duration: 3
          });
        })
        .then(function() {
          $scope.getProfile();
        })
        .catch(function(response) {
          $alert({
            content: response.data.message,
            animation: 'fadeZoomFadeDown',
            type: 'material',
            duration: 3
          });
        });
    };

    /**
     * Unlink third-party provider.
     */
    $scope.unlink = function(provider) {
      $auth.unlink(provider)
        .then(function() {
          $alert({
            content: 'You have successfully unlinked ' + provider + ' account',
            animation: 'fadeZoomFadeDown',
            type: 'material',
            duration: 3
          });
        })
        .then(function() {
          $scope.getProfile();
        })
        .catch(function(response) {
          $alert({
            content: response.data ? response.data.message : 'Could not unlink ' + provider + ' account',
            animation: 'fadeZoomFadeDown',
            type: 'material',
            duration: 3
          });
        });
    };

    $scope.getProfile();

  });